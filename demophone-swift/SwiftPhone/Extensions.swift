import Foundation
import Softphone_Swift

//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
extension Data
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
{
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    var hexString: String
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        let hexString = map { String(format: "%02.2hhx", $0) }.joined()
        return hexString
    }
}

//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
extension Dictionary
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
{
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    static func prepareNode(node: XmlTree, dictValue: Any)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        if dictValue is NSString
        {
            node.data = dictValue as? String
        }
        else if dictValue is NSNumber
        {
            node.setAttribute(name: "type", value: String(describing: dictValue))
        }
        else if dictValue is NSArray
        {
            let arr = dictValue as! NSArray
            for x in 0..<arr.count
            {
                let item = XmlTree(name: "item")
                prepareNode(node: item!, dictValue: arr[x])
                node.addNode(item)
            }
        }
        else if dictValue is NSDictionary
        {
            let dict = dictValue as! NSDictionary
            let keys = dict.allKeys as! [String]
            
            for key in keys
            {
                let item = XmlTree(name: key)
                prepareNode(node: item!, dictValue: dict.value(forKey: key)!)
                node.addNode(item)
            }
        }
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    static func xmlFromDictionary(_ dictionary: [AnyHashable : Any]) -> XmlTree
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        let xml = XmlTree(name: "root")!
        prepareNode(node: xml, dictValue: dictionary)
        return xml
    }
}
