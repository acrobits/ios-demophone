import Foundation
import Softphone_Swift

//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
protocol TransferChangedDelegate: AnyObject
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
{
    func onTransferStarted(_ call: SoftphoneCallEvent)
    func onTransferCancelled(_ call: SoftphoneCallEvent)
}

//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
protocol AttendedTransferChangedDelegate: AnyObject
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
{
    func onAttendedTransferStarted(_ call: SoftphoneCallEvent)
    func onAttendedTransferCancelled(_ call: SoftphoneCallEvent)
}

//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
protocol ForwardChangedDelegate: AnyObject
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
{
    func onForwardStarted(_ call: SoftphoneCallEvent)
    func onForwardCancelled(_ call: SoftphoneCallEvent)
}

//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
class CallActionHelpers
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
{
    static let shared = CallActionHelpers()
    
    private var transferChangedDelegates = NSHashTable<AnyObject>.weakObjects()
    private var attendedTransferChangedDelegates = NSHashTable<AnyObject>.weakObjects()
    private var forwardChangedDelegates = NSHashTable<AnyObject>.weakObjects()
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func addTransferChangedDelegate(_ delegate: TransferChangedDelegate)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        transferChangedDelegates.add(delegate)
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func removeTransferChangedDelegate(_ delegate: TransferChangedDelegate)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        transferChangedDelegates.remove(delegate)
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func addAttendedTransferChangedDelegate(_ delegate: AttendedTransferChangedDelegate)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        attendedTransferChangedDelegates.add(delegate)
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func removeAttendedTransferChangedDelegate(_ delegate: AttendedTransferChangedDelegate)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        attendedTransferChangedDelegates.remove(delegate)
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func addForwardChangedDelegate(_ delegate: ForwardChangedDelegate)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        forwardChangedDelegates.add(delegate)
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func removeForwardChangedDelegate(_ delegate: ForwardChangedDelegate)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        forwardChangedDelegates.remove(delegate)
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func beginTransfer(_ call: SoftphoneCallEvent)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        if let callTransients = call.transients {
            callTransients.set("1", forKey: "markedForTransfer")
            call.transients = callTransients
        }
        
        if let callHoldStates = SoftphoneBridge.instance().calls().isHeld(call), callHoldStates.local != CallHoldState_Held {
            SoftphoneBridge.instance().calls().setHeld(call, held: true)
        }
        
        notifyTransferStarted(call)
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func cancelTransfer(_ call: SoftphoneCallEvent)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        if let callTransients = call.transients {
            callTransients.remove(key: "markedForTransfer")
            call.transients = callTransients
        }
        
        if let callHoldStates = SoftphoneBridge.instance().calls().isHeld(call), callHoldStates.local == CallHoldState_Held {
            SoftphoneBridge.instance().calls().setHeld(call, held: false)
        }
        
        notifyTransferCancelled(call)
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func isTransferring(_ call: SoftphoneCallEvent) -> Bool
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        return call.transients.get(key: "markedForTransfer").asString() == "1"
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func beginAttendedTransfer(_ call: SoftphoneCallEvent)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        if let callTransients = call.transients {
            callTransients.set("1", forKey: "markedForAttendedTransfer")
            call.transients = callTransients
        }
        
        if let callHoldStates = SoftphoneBridge.instance().calls().isHeld(call), callHoldStates.local != CallHoldState_Held {
            SoftphoneBridge.instance().calls().setHeld(call, held: true)
        }
        
        notifyAttendedTransferStarted(call)
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func cancelAttendedTransfer(_ call: SoftphoneCallEvent)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        if let callTransients = call.transients {
            callTransients.remove(key: "markedForAttendedTransfer")
            call.transients = callTransients
        }
        
        if let callHoldStates = SoftphoneBridge.instance().calls().isHeld(call), callHoldStates.local == CallHoldState_Held {
            SoftphoneBridge.instance().calls().setHeld(call, held: false)
        }
        
        notifyAttendedTransferCancelled(call)
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func isAttendedTransferring(_ call: SoftphoneCallEvent) -> Bool
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        return call.transients.get(key: "markedForAttendedTransfer").asString() == "1"
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func beginForward(_ call: SoftphoneCallEvent)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        if let callTransients = call.transients {
            callTransients.remove(key: "markedForFowarding")
            call.transients = callTransients
        }
        
        notifyForwardStarted(call)
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func cancelForward(_ call: SoftphoneCallEvent)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        if let callTransients = call.transients {
            callTransients.remove(key: "markedForFowarding")
            call.transients = callTransients
        }
        
        notifyForwardCancelled(call)
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func isForwarding(_ call: SoftphoneCallEvent) -> Bool
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        return call.transients.get(key: "markedForFowarding").asString() == "1"
    }
}

//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
private extension CallActionHelpers
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
{
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func notifyTransferStarted(_ call: SoftphoneCallEvent)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        for delegate in transferChangedDelegates.allObjects {
            (delegate as? TransferChangedDelegate)?.onTransferStarted(call)
        }
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func notifyTransferCancelled(_ call: SoftphoneCallEvent)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        for delegate in transferChangedDelegates.allObjects {
            (delegate as? TransferChangedDelegate)?.onTransferCancelled(call)
        }
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func notifyAttendedTransferStarted(_ call: SoftphoneCallEvent)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        for delegate in attendedTransferChangedDelegates.allObjects {
            (delegate as? AttendedTransferChangedDelegate)?.onAttendedTransferStarted(call)
        }
    }
    
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    func notifyAttendedTransferCancelled(_ call: SoftphoneCallEvent)
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
        for delegate in attendedTransferChangedDelegates.allObjects {
            (delegate as? AttendedTransferChangedDelegate)?.onAttendedTransferCancelled(call)
