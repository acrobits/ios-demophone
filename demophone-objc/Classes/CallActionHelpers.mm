//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
-(void)beginTransfer:(Softphone::EventHistory::CallEvent::Pointer)call
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
{
    if (call.is_null())
        return;
    
    call->transients["markedForTransfer"_s] = "1"_s;
    
    if (Softphone::instance()->calls()->isHeld(call).local != Call::HoldState::Held)
        Softphone::instance()->calls()->setHeld(call, true);
    
    [self notifyTransferStarted:call];
}

//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
-(void)cancelTransfer:(Softphone::EventHistory::CallEvent::Pointer)call
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
{
    if (call.is_null())
        return;
    
    call->transients.erase("markedForTransfer"_s);
    
    if (Softphone::instance()->calls()->isHeld(call).local == Call::HoldState::Held)
        Softphone::instance()->calls()->setHeld(call, false);
    
    [self notifyTransferCancelled:call];
}

//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
-(BOOL)isTransferring:(Softphone::EventHistory::CallEvent::Pointer)call
//-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
{
    if (call.is_null())
        return false;
    
    return call->transients["markedForTransfer"_s] == "1"_s;
}

